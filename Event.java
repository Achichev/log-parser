package logs_parser;

public enum Event {
    LOGIN,
    DOWNLOAD_PLUGIN,
    WRITE_MESSAGE,
    SOLVE_TASK,
    DONE_TASK
}
/*
 * The enum contains each event description.
 * LOGIN - user logged in,
 * DOWNLOAD_PLUGIN - user downloaded plugin,
 * WRITE_MESSAGE - user wrote message,
 * SOLVE_TASK - user tried to solve task,
 * DONE_TASK - user has solved task.
 */
